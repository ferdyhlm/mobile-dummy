package com.example.hazelnut.dummy;

public class Status {
    private String bankStatus;
    private String insuranceStatus;
    private String financeStatus;
    private String syariahStatus;
    private String sekuritasStatus;

    public String getBankStatus() {
        return bankStatus;
    }

    public void setBankStatus(String bankStatus) {
        this.bankStatus = bankStatus;
    }

    public String getInsuranceStatus() {
        return insuranceStatus;
    }

    public void setInsuranceStatus(String insuranceStatus) {
        this.insuranceStatus = insuranceStatus;
    }

    public String getFinanceStatus() {
        return financeStatus;
    }

    public void setFinanceStatus(String financeStatus) {
        this.financeStatus = financeStatus;
    }

    public String getSyariahStatus() {
        return syariahStatus;
    }

    public void setSyariahStatus(String syariahStatus) {
        this.syariahStatus = syariahStatus;
    }

    public String getSekuritasStatus() {
        return sekuritasStatus;
    }

    public void setSekuritasStatus(String sekuritasStatus) {
        this.sekuritasStatus = sekuritasStatus;
    }
}
